enum CarDirections {
  /// Mặt trái - trước xe
  leftHead,

  /// Mặt trái - sau xe
  leftTail,

  /// Mặt phải - trước xe
  righteHead,

  /// Mặt phải - sau xe
  rightTail,

  /// Mặt trước xe góc 45 độ trái
  left45DegreeFrontOfCar,

  /// Mặt trước xe góc thẳng
  frontOfCar,

  /// Mặt trước xe góc 45 độ phải
  right45DegreeFrontOfCar,

  /// Mặt sau xe góc 45 độ trái
  left45DegreeBehindCar,

  /// Mặt sau xe góc thẳng
  behindCar,

  /// Mặt sau xe góc 45 độ phải
  right45DegreeBehindCar,
}
